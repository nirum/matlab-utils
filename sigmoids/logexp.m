% f = logistic(x, {'gain', 1, 'slope', 1, 'threshold', 0})

%function f = logexp(x, varargin)

%p = inputParser;
%addRequired(p,'x',@isnumeric);
%addParamValue(p,'gain',1,@isnumeric);
%addParamValue(p,'threshold',0,@isnumeric);
%addParamValue(p,'slope',1,@isnumeric);
%parse(p,x,varargin{:});

%f = p.Results.gain.*log(1+exp(p.Results.slope*(p.Results.x-p.Results.threshold)));
